package session1_Introduction;
import java.lang.*; // импортируемая библиотека

public class MyFirst {
    // public - метод виден вне класса
    // static - метод виден вне класса, но использование не влечет создание объеккта
    public static void main (String[] arg) {
        // println - метод, который выводит что-то на экран, следовательно, нужны скобки
        // out - метод
        // System - класс
        System.out.println("hello world");
    }
}
